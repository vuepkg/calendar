// Generates the public API reference table for ScheduleCalendar.vue from its
// TypeScript types via vue-component-meta, so the docs site can't drift from
// the actual props/emits/slots (F3-2 / REV "문서 drift" finding).
//
// Regenerate: pnpm --filter @vuepkg/calendar run docs:api
// CI drift check: pnpm --filter @vuepkg/calendar run docs:api:check
//
// DO NOT hand-edit the generated output file — edit the JSDoc comments on
// ScheduleCalendar.vue / calendarEvents.ts / slots.ts instead and regenerate.

import pkg from 'vue-component-meta'
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const { createChecker } = pkg

const packageRoot = path.dirname(path.dirname(fileURLToPath(import.meta.url)))
const componentFile = path.join(
  packageRoot,
  'src/components/calendar/ScheduleCalendar.vue',
)
const outFile = path.join(
  packageRoot,
  '../../apps/docs/api/_generated/schedule-calendar-api.md',
)

const checker = createChecker(path.join(packageRoot, 'tsconfig.app.json'), {
  forceUseTs: true,
  printer: { newLine: 1 },
})

const meta = checker.getComponentMeta(componentFile)

function escapeCell(text) {
  if (!text) return ''
  // Negative lookbehind skips pipes some source JSDoc already hand-escaped
  // (e.g. "`2`\|`3`") so re-generation doesn't pile up extra backslashes.
  return String(text)
    .replace(/\r?\n/g, '<br>')
    .replace(/(?<!\\)\|/g, '\\|')
}

// GFM table cells split on literal `|` even inside backtick code spans, so
// type unions like `string | undefined` must be pipe-escaped too.
function code(text) {
  if (text === undefined || text === null || text === '') return '—'
  return `\`${escapeCell(text)}\``
}

// vue-component-meta cannot trace JSDoc through `defineEmits<ExternalInterface>()`
// back to the interface member declarations (only inline defineProps/defineSlots
// object-literal members resolve JSDoc). Existence and payload *types* below are
// still fully derived from the types, so they can't drift — only this prose is a
// manually-kept fallback. Source of truth for the wording: calendarEvents.ts JSDoc.
const emitDescriptions = {
  'view-change': 'Month/Week/Day/List 탭 클릭',
  'date-select': '월간 셀, Week 날짜 헤더 클릭',
  'overflow-click': '월간 `+N` 클릭',
  'schedule-click': '칩·All Day 바·시간 일정·List 행 클릭',
  navigate: 'Today·‹›(이전/다음) 버튼 클릭 — Month/Week/Day',
  'list-filter-clear': 'List 뷰 날짜 필터 바 Clear 클릭',
  'query-change': 'View Option·Schedule Type·기간 포함 — 부모 API 조회용',
  'time-slot-select': 'Week/Day 시간 그리드 빈 셀 클릭 — 일정 생성 등에 start/end 전달',
  'schedule-move': 'Week/Day 시간 그리드 — 드래그로 이벤트 이동 확정',
  'schedule-resize': 'Week/Day 시간 그리드 — 드래그로 이벤트 end 시각 리사이즈 확정',
}

/** Strips a trailing `| undefined` once a runtime default guarantees the value is set. */
function displayType(prop) {
  if (prop.default === undefined) return prop.type
  return prop.type.replace(/\s*\|\s*undefined\s*$/, '')
}

/** `"[payload: X]"` -> `"X"`, `"[]"` -> `undefined` (no payload). */
function extractPayloadType(tupleType) {
  const inner = tupleType.replace(/^\[/, '').replace(/\]$/, '').trim()
  if (!inner) return undefined
  const match = inner.match(/^\w+:\s*(.+)$/)
  return match ? match[1] : inner
}

const modelNames = new Set(
  meta.events
    .filter((e) => e.name.startsWith('update:'))
    .map((e) => e.name.slice('update:'.length)),
)

const props = meta.props.filter((p) => !p.global && !modelNames.has(p.name))
const models = meta.props.filter((p) => !p.global && modelNames.has(p.name))
const emits = meta.events.filter((e) => !e.name.startsWith('update:'))
const slots = meta.slots

const lines = []
lines.push(
  '<!-- AUTO-GENERATED by `pnpm --filter @vuepkg/calendar run docs:api` (vue-component-meta). -->',
  '<!-- DO NOT EDIT BY HAND — edit the JSDoc comments on ScheduleCalendar.vue and regenerate. -->',
  '',
  '## Props',
  '',
  '| prop | 타입 | 기본값 | 필수 | 설명 |',
  '|------|------|--------|:---:|------|',
)
for (const p of props) {
  lines.push(
    `| \`${p.name}\` | ${code(displayType(p))} | ${code(p.default)} | ${p.required ? '✅' : ''} | ${escapeCell(p.description)} |`,
  )
}

lines.push('', '## v-model', '', '| v-model | 타입 | 기본값 | 설명 |', '|---------|------|--------|------|')
for (const p of models) {
  lines.push(
    `| \`v-model:${p.name}\` | ${code(displayType(p))} | ${code(p.default)} | ${escapeCell(p.description)} |`,
  )
}

lines.push('', '## Emits', '', '| emit | payload | 설명 |', '|------|---------|------|')
for (const e of emits) {
  const description = e.description || emitDescriptions[e.name] || ''
  lines.push(`| \`${e.name}\` | ${code(extractPayloadType(e.type))} | ${escapeCell(description)} |`)
}

lines.push(
  '',
  '## Slots',
  '',
  '> 상세 slot props 필드는 [architecture.md § Scoped Slots](https://github.com/vuepkg/calendar/blob/main/docs/dev/architecture.md#scoped-slots-rev-a1-2026-07-02) 참고.',
  '',
  '| slot | props 타입 | 설명 |',
  '|------|-----------|------|',
)
for (const s of slots) {
  lines.push(`| \`#${s.name}\` | ${code(s.type)} | ${escapeCell(s.description)} |`)
}

lines.push('')

fs.mkdirSync(path.dirname(outFile), { recursive: true })
fs.writeFileSync(outFile, lines.join('\n'))

console.log(`[generate-api-docs] wrote ${path.relative(packageRoot, outFile)}`)
console.log(
  `[generate-api-docs] ${props.length} props, ${models.length} v-model, ${emits.length} emits, ${slots.length} slots`,
)
